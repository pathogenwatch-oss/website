function mapVisToSigma({ data, nodeStyles }) {
  return {
    nodes: data.nodes.map(node => ({
      ...node,
      style: {
        colour: nodeStyles[node.id].colour,
        shape: nodeStyles[node.id].shape,
      },
      size: 1,
    })),
    edges: data.edges.map(edge => ({
      id: edge.id,
      source: edge.from,
      target: edge.to,
      style: {
        colour: '#ccc',
      },
      type: 'curve',
    })),
  };
}

const cdcProps = require('./data/cdc.json');

export const cdcGraph = mapVisToSigma(cdcProps);
